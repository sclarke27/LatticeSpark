#!/usr/bin/env node
// Module Manifest Generator
//
// Scans modules/*/module.json for modules with ui.page: true,
// checks for a corresponding ui/<id>-page.js file, and generates
// web/src/module-manifest.js with lazy-import entries.
//
// Run: node scripts/generate-module-manifest.js

import { readdir, readFile, writeFile } from 'fs/promises';
import { join, dirname } from 'path';
import { existsSync } from 'fs';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const ROOT = join(__dirname, '..');
const MODULES_DIR = join(ROOT, 'modules');
const OUTPUT = join(ROOT, 'web', 'src', 'module-manifest.js');

async function generate() {
  const entries = [];

  let dirs;
  try {
    dirs = await readdir(MODULES_DIR, { withFileTypes: true });
  } catch {
    console.log('No modules/ directory found — generating empty manifest.');
    dirs = [];
  }

  for (const dirent of dirs) {
    if (!dirent.isDirectory()) continue;

    const moduleId = dirent.name;

    // Validate module ID is safe for use in generated code
    if (!/^[a-z][a-z0-9-]*$/.test(moduleId)) {
      console.warn(`  Warning: Invalid module ID "${moduleId}" — skipping`);
      continue;
    }

    const configPath = join(MODULES_DIR, moduleId, 'module.json');

    if (!existsSync(configPath)) continue;

    try {
      const raw = await readFile(configPath, 'utf-8');
      const config = JSON.parse(raw);

      if (!config.ui?.page) continue;

      const pageFile = join(MODULES_DIR, moduleId, 'ui', `${moduleId}-page.js`);
      if (!existsSync(pageFile)) {
        console.warn(`  Warning: ${moduleId} has ui.page: true but no ui/${moduleId}-page.js — skipping`);
        continue;
      }

      entries.push(moduleId);
      console.log(`  Found module page: ${moduleId} ("${config.ui.label || config.name}")`);
    } catch (err) {
      console.warn(`  Warning: Failed to read ${configPath}: ${err.message}`);
    }
  }

  const imports = entries
    .map(id => `  '${id}': () => import('../../modules/${id}/ui/${id}-page.js'),`)
    .join('\n');

  const content = `// Auto-generated by scripts/generate-module-manifest.js — do not edit
export const modulePages = {
${imports}
};
`;

  await writeFile(OUTPUT, content, 'utf-8');
  console.log(`\nGenerated ${OUTPUT} with ${entries.length} module page(s).`);
}

generate().catch(err => {
  console.error('Failed to generate module manifest:', err);
  process.exit(1);
});
